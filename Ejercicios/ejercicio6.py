#Preguntar al usuario un string e imprimir si esta es un palíndromo (una palabra que se lee igual empezando por el principio que por el final)

palabra = input("escriba una lista de caracteres y le pondré en una lista todas aquellas que sean palíndromas: ")
if (palabra) == (palabra)[::-1]:
    print("Es palindroma")
else:
    print("No es palindroma")

